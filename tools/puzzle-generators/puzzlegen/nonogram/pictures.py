def _to_grid(rows: list[str]) -> list[list[int]]:
    return [[1 if ch == "1" else 0 for ch in row] for row in rows]


def curated_puzzles() -> list[dict]:
    """Return all curated pictures as decoded puzzle dicts with a `size` field."""
    out: list[dict] = []
    for entry in (*PICTURES_10, *PICTURES_15):
        grid = _to_grid(entry["grid"])
        size = len(grid)
        # Only keep uniquely-solvable (line-solvable) pictures.
        if not is_line_solvable(grid):
            continue
        row_clues = [clue_from_line(grid[r]) for r in range(size)]
        col_clues = [clue_from_line([grid[r][c] for r in range(size)]) for c in range(size)]
        out.append({
            "title": entry["title"],
            "category": entry["category"],
            "size": size,
            "solution": grid,
            "rowClues": [{"values": v} for v in row_clues],
            "columnClues": [{"values": v} for v in col_clues],
        })
    return out


if __name__ == "__main__":
    kept = curated_puzzles()
    by_size: dict[int, int] = {}
    for p in kept:
        by_size[p["size"]] = by_size.get(p["size"], 0) + 1
    print(f"curated pictures kept (uniquely solvable): {len(kept)} -> {by_size}")
